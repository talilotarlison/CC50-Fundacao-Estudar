# Exemplo de swap em Python

# Definindo duas variáveis
a = 5
b = 10

# Exibindo os valores antes do swap
print("Antes do swap:")
print("a =", a)
print("b =", b)

# Realizando o swap
a, b = b, a

# Exibindo os valores após o swap
print("Depois do swap:")
print("a =", a)
print("b =", b)